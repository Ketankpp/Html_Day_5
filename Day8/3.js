// Boolean Condition
const flag = true;
if (flag) {
  console.log("Boolean condition is true");
} else {
  console.log("Boolean condition is false");
}

// String Condition
const text = "Hello";
if (text === "Hello") {
  console.log("String condition is true");
} else {
  console.log("String condition is false");
}

// Number Condition
const num = 10;
if (num > 5) {
  console.log("Number condition is true");
} else {
  console.log("Number condition is false");
}

// Object Condition
const person = {
  name: "Ketan",
  age: 22,
};

if (person.age >= 18) {
  console.log("Object condition is true");
} else {
  console.log("Object condition is false");
}
